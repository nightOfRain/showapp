package com.nantian.plugins;

import java.util.HashMap;
import java.util.Map;

import org.apache.cordova.CallbackContext;
import org.apache.cordova.CordovaInterface;
import org.apache.cordova.CordovaPlugin;
import org.apache.cordova.CordovaWebView;
import org.json.JSONArray;
import org.json.JSONException;

public class NTDataDictionaryPlugin extends CordovaPlugin {
	public static Map<String,String> dic=new HashMap<String, String>();

	/**
	 * Constructor.
	 */
	public NTDataDictionaryPlugin() {
	}

	/**
	 * Sets the context of the Command. This can then be used to do things like
	 * get file paths associated with the Activity.
	 * 
	 * @param cordova
	 *            The context of the main Activity.
	 * @param webView
	 *            The CordovaWebView Cordova is running in.
	 */
	public void initialize(CordovaInterface cordova, CordovaWebView webView) {
		super.initialize(cordova, webView);
	}

	/**
	 * Executes the request and returns PluginResult.
	 * 
	 * @param action
	 *            The action to execute.
	 * @param args
	 *            JSONArry of arguments for the plugin.
	 * @param callbackContext
	 *            The callback id used when calling back into JavaScript.
	 * @return True if the action was valid, false if not.
	 */
	public boolean execute(String action, JSONArray args,
			CallbackContext callbackContext) throws JSONException {
		if (action.equals("addValue")) {
			dic.put(args.getString(0), args.getString(1));
			//callbackContext.success("2");
		}else if(action.equals("getValue")){
			String s=dic.get(args.getString(0));
			callbackContext.success(s);
		}else if(action.equals("delValue")){
			dic.remove(args.get(0));
		}else if(action.equals("deleteAll")){
			dic.clear();
		}

		
		return true;
	}
}